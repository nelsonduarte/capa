# Injected runtime helper (for `?`)
# ===========================================================

_TRY_HELPER = '''
class _CapaTryEarlyReturn(Exception):
    """Internal exception used by the ? operator to propagate Err.

    The _capa_run wrapper catches it at each function's boundary and
    converts it back into an Err return. The Capa programmer never sees
    this exception.
    """
    def __init__(self, err):
        self.err = err

def _capa_try(result):
    if isinstance(result, Err):
        raise _CapaTryEarlyReturn(result)
    if isinstance(result, Ok):
        return result.value
    raise RuntimeError(f"`?` applied to non-Result value: {result!r}")

def _capa_wrap(fn):
    """Decorator that catches _CapaTryEarlyReturn and returns the
    corresponding Err. Applied by the transpiler to functions that use `?`.
    """
    def wrapper(*args, **kwargs):
        try:
            return fn(*args, **kwargs)
        except _CapaTryEarlyReturn as e:
            return e.err
    wrapper.__name__ = getattr(fn, "__name__", "wrapper")
    return wrapper
'''


def _uses_try(node) -> bool:
    """Determines whether an AST subtree contains any Try expression.

    Recursively visits the subtree. Does not cross function boundaries
    (a nested fun with ? does not force the outer fun to have the
    decorator). In Capa v1 there are no nested functions, but the rule
    is here just in case.
    """
    from . import capa_ast as A

    if isinstance(node, A.Try):
        return True
    if isinstance(node, (A.FunDecl,)):
        return False  # function boundary
    if isinstance(node, list):
        return any(_uses_try(x) for x in node)
    if hasattr(node, "__dataclass_fields__"):
        for f in node.__dataclass_fields__:
            if f == "pos":
                continue
            v = getattr(node, f)
            if _uses_try(v):
                return True
    if isinstance(node, tuple):
        return any(_uses_try(x) for x in node)
    return False


def transpile(
    module: A.Module,
    filename: str = "<input>",
    types: Optional[dict[int, Ty]] = None,
) -> str:
    """Transpiles a Capa Module to a Python source string.

    The output includes the prelude (runtime imports) and the helper
    for `?`. If ``types`` is passed (from AnalyzeResult), it is used
    for type-aware dispatch on method calls.
    """
    t = Transpiler(filename=filename, types=types)
    code = t.transpile(module)
    # Insert the `?` helper right after the prelude. It is ugly but
    # simple; a future version would emit everything via structured blocks.
    lines = code.split("\n")
    insert_at = 0
    for i, line in enumerate(lines):
        if line.startswith("from capa.runtime"):
            # Find the closing of the import (closing parenthesis).
            j = i
            while j < len(lines) and ")" not in lines[j]:
                j += 1
            insert_at = j + 1
            break
    if insert_at == 0:
        insert_at = len(lines)
    new_lines = lines[:insert_at] + _TRY_HELPER.split("\n") + lines[insert_at:]
    return "\n".join(new_lines)

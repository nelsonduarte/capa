"""Transpiler from Capa to Python 3.12+.

Strategy: walk the AST after semantic analysis, emitting Python code
line by line through an ``Emitter`` that handles indentation automatically.
The output is intended to be executed by a Python 3.10 or later
interpreter (for match/case).

Main mappings:

- Functions and methods: Python ``def`` without type annotations (simpler
  and avoids issues with generic types / capabilities whose exact
  signature does not always have an equivalent in ``typing``).
- ``let`` and ``var``: indistinguishable in Python, both emit ``x = expr``.
  The immutability of ``let`` was verified statically by the analyzer.
- ``type T { ... }`` (struct): ``@dataclass`` with the fields.
- ``type T = A | B(P) | ...`` (sum): each variant is a class. For
  variants with a payload, it is a dataclass with a single ``value`` field.
  The type name becomes a union (not emitted, Capa does not need it at
  runtime, and Python does not require it).
- ``trait``: abstract ABC with abstract methods (informative). The impls
  generate concrete subclasses.
- ``impl T``: methods added as class functions.
- ``impl Trait for T``: class T inherits from the corresponding trait.
- ``match``: Python ``match/case``.
- ``?`` (try): local expansion with ``if isinstance(_t, Err): return _t``.
- Strings with ``${expr}``: translated to f-strings; ``$$`` escapes ``$``.
- Constant variants (``Red``): call ``Red()`` (instance).
- Capabilities and types imported from the runtime stdlib: a single
  ``from capa.runtime import *`` is emitted at the top of the generated file.

Known limitations (v1):

- No optimizations: the output is reasonably readable but is not
  "idiomatic Python", it is Python that closely mirrors the Capa
  structure.
- No tail call detection.
- ``self`` in methods is translated directly; Capa "self" and Python
  "self" align naturally.
- The ``?`` operator requires the enclosing function to return ``Result``.
  If it does not, the generated code will produce an early return of an
  Err, useful as a runtime diagnostic, but not detected at transpilation
  time in v1.
"""

from __future__ import annotations

import re
from typing import Optional

from . import capa_ast as A
from .typesys import Ty, TyName


# ===========================================================
# Header of the generated file
# ===========================================================

_PRELUDE = '''\
# Generated by capa transpiler. Do not edit by hand.
# Generated from: {filename}

from __future__ import annotations
from dataclasses import dataclass, field
from capa.runtime import (
    Ok, Err, Result, Some, None_, Option, IoError,
    Stdio, Fs, Env, Clock, Random, Net, Proc, Db, Unsafe,
    CapaList, _NoneType,
    parse_int, parse_float,
    to_float, to_int,
    py_import, py_invoke,
    JNull, JBool, JNum, JStr, JArr, JObj, JsonValue,
    parse_json, to_json,
)
'''


class TranspilerError(Exception):
    """Transpiler error (an unsupported transpilation case)."""
    pass


# ===========================================================
# Emitter: handles indentation and lines
# ===========================================================


class Emitter:
    """Accumulator of output lines with automatic indentation."""

    def __init__(self, indent_unit: str = "    "):
        self.lines: list[str] = []
        self._indent = 0
        self._unit = indent_unit

    def write(self, line: str) -> None:
        if line == "":
            self.lines.append("")
        else:
            self.lines.append(self._unit * self._indent + line)

    def blank(self) -> None:
        # Only add a blank line if the last one is not already blank.
        if self.lines and self.lines[-1] != "":
            self.lines.append("")

    def indent(self) -> None:
        self._indent += 1

    def dedent(self) -> None:
        self._indent -= 1
        assert self._indent >= 0

    def get(self) -> str:
        # Ensures the file ends with a newline and has no trailing
        # blank lines.
        text = "\n".join(self.lines).rstrip() + "\n"
        return text


